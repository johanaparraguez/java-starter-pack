    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed

        //Obtenemos los datos del formulario
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String edad = txtEdad.getText();
        String sexo = txtSexo.getText();
        String nombreMascota = txtNombreMascota.getText();
        String sexoMascota = txtSexoMascota.getText();
        
        //Validamos que no esten vacio los campos
        if (!nombre.isEmpty() && !apellido.isEmpty() && !edad.isEmpty() && !sexo.isEmpty() && !nombreMascota.isEmpty() && !sexoMascota.isEmpty()) {
            
            ClaseVeterinaria vet = new ClaseVeterinaria(nombre, apellido, edad, sexo, nombreMascota, sexoMascota);
            
            Crud c = new Crud();
            
            try {
                c.insertar(vet);
            } catch (SQLException ex) {
                Logger.getLogger(FrameInsertar.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            
            
        }else{
            JOptionPane.showMessageDialog(null, "Debes completar todo el formulario");
        }
        
        
        
        
    }//GEN-LAST:event_btnGrabarActionPerformed

